let currentQuestions = [];
let selectedLessons = "";

function startTest() {

 let selected = [];

 for(let i=5;i<=14;i++){

   if(document.getElementById("lesson"+i).checked){
     selected.push(String(i));
   }

 }

 selectedLessons =
 selected.map(
   n => "第" + n + "回"
 ).join("・");

 currentQuestions =
 questions.filter(
   q => selected.includes(q.lesson)
 );

 currentQuestions =
 currentQuestions.sort(
   () => Math.random() - 0.5
 );

 currentQuestions =
 currentQuestions.slice(0,15);

 document.getElementById("setup").style.display = "none";

 let html = "";

 currentQuestions.forEach((q,index)=>{

   html += "<p>";

   html += "【第" + q.lesson + "回】<br>";

   html += (index + 1) + ". ";

   html += q.question;

   html += "</p>";

   html += '<input id="ans'+index+'"><br><br>';

 });

 html += '<button onclick="gradeTest()">採点する</button>';

 document.getElementById("quiz").innerHTML =
 html;

}

function gradeTest(){

 let score = 0;
 let result = "";

 currentQuestions.forEach((q,index)=>{

   let userAnswer =
   document.getElementById(
     "ans"+index
   ).value.trim();

   if(
     Array.isArray(q.answer)
     ? q.answer.includes(userAnswer)
     : userAnswer === q.answer
   ){

     score++;

   }else{

     result +=
     "【第" + q.lesson + "回】<br>" +
     q.question +
     "<br>正解：" +
     (
       Array.isArray(q.answer)
       ? q.answer.join(" / ")
       : q.answer
     ) +
     "<br><br>";

   }

 });

 let rate =
 Math.round(
   score /
   currentQuestions.length *
   100
 );

 let studentName =
 document.getElementById(
   "studentName"
 ).value;

 let grade =
 document.getElementById(
   "grade"
 ).value;

 let className =
 document.getElementById(
   "className"
 ).value;

 let number =
 document.getElementById(
   "number"
 ).value;

 fetch(
 "https://script.google.com/macros/s/AKfycbyhfc71xWTxJdz6OU37MBp-xfYlneNrFdEgKiokH3B-vylkc2S3Q32RGZUPhK11FLqr/exec",
 {
   method:"POST",
   mode:"no-cors",
   body:JSON.stringify({
     name: studentName,
     grade: grade,
     classname: className,
     number: number,
     test: selectedLessons,
     score: score,
     rate: rate,
     questionCount: currentQuestions.length
   })
 }
 );

 document.getElementById("quiz").innerHTML =

 "<h2>結果</h2>" +

 "<p>氏名：" + studentName + "</p>" +

 "<p>学年：" + grade + "</p>" +

 "<p>クラス：" + className + "</p>" +

 "<p>出席番号：" + number + "</p>" +

 "<p>得点：" +
 score +
 " / " +
 currentQuestions.length +
 "</p>" +

 "<p>正答率：" +
 rate +
 "%</p>" +

 "<h3>間違えた問題</h3>" +

 result +

 '<button onclick="location.reload()">新しいテストを始める</button>';

}
